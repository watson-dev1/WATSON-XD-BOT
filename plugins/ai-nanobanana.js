import uploadImage from '../lib/uploadImage.js';
import fetch from 'node-fetch';

let handler = async (m, { conn, text, usedPrefix, command }) => {
    // Check if the user replied to a message
    const q = m.quoted ? m.quoted : m;
    const mime = (q.msg || q).mimetype || q.mediaType || '';

    // Ensure the replied message is an image
    if (!/image/.test(mime)) {
        throw `Incorrect format! Reply to an image with the caption:\n*${usedPrefix + command} <prompt>*\n\n*Example:*\n${usedPrefix + command} to ghibli style art`;
    }

    // Ensure the user provided a prompt
    if (!text) {
        throw `Prompt cannot be empty! Reply to an image with the caption:\n*${usedPrefix + command} <prompt>*\n\n*Example:*\n${usedPrefix + command} to ghibli style art`;
    }

    // Validate prompt length
    if (text.length > 500) {
        throw `Prompt is too long! Maximum 500 characters.`;
    }

    let processingMsg;
    try {
        processingMsg = await m.reply('⏳ Processing image with Nano-Banana AI...');

        // Download and upload the image
        const img = await q.download();
        if (!img || img.length === 0) {
            throw new Error('Failed to download image!');
        }

        // Validate file size (max 10MB)
        if (img.length > 10 * 1024 * 1024) {
            throw new Error('Image size is too large! Maximum 10MB.');
        }

        const imageUrl = await uploadImage(img);
        if (!imageUrl) {
            throw new Error('Failed to upload image to the server!');
        }

        // Validate image URL
        if (!imageUrl.startsWith('http')) {
            throw new Error('Invalid image URL!');
        }

        // Update processing status
        await conn.sendMessage(m.chat, { 
            text: '🎨 Image successfully uploaded, processing with AI...', 
            edit: processingMsg.key 
        });

        // Function to make a request with retries
        const makeRequest = async (retries = 3) => {
            for (let i = 0; i < retries; i++) {
                try {
                    const controller = new AbortController();
                    const timeoutId = setTimeout(() => controller.abort(), 60000); // 60 seconds timeout

                    const apiUrl = `https://api.platform.web.id/nano-banana?imageUrl=${encodeURIComponent(imageUrl)}&prompt=${encodeURIComponent(text)}`;
                    
                    const response = await fetch(apiUrl, {
                        method: 'GET',
                        headers: {
                            'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36',
                            'Accept': 'application/json',
                            'Connection': 'keep-alive'
                        },
                        signal: controller.signal,
                        timeout: 60000
                    });

                    clearTimeout(timeoutId);

                    if (!response.ok) {
                        throw new Error(`HTTP ${response.status}: ${response.statusText}`);
                    }

                    const json = await response.json();
                    return json;

                } catch (error) {
                    console.log(`Attempt ${i + 1} failed:`, error.message);
                    
                    if (i === retries - 1) throw error;
                    
                    // Wait before retrying
                    await new Promise(resolve => setTimeout(resolve, 2000 * (i + 1)));
                    
                    // Update retry status
                    await conn.sendMessage(m.chat, { 
                        text: `🔄 Retrying... (${i + 2}/${retries})`, 
                        edit: processingMsg.key 
                    });
                }
            }
        };

        // Call API with retry mechanism
        const json = await makeRequest();

        // Validate response
        if (!json) {
            throw new Error('Empty response from server');
        }

        if (!json.success) {
            throw new Error(json.message || 'API returned a failed status');
        }

        if (!json.result || !json.result.results || !Array.isArray(json.result.results)) {
            throw new Error('Invalid response format from API');
        }

        if (json.result.results.length === 0) {
            throw new Error('No results were generated by AI');
        }

        const resultUrl = json.result.results[0]?.url;
        if (!resultUrl || !resultUrl.startsWith('http')) {
            throw new Error('Invalid result URL');
        }

        // Update final status
        await conn.sendMessage(m.chat, { 
            text: '✅ Success! Sending the result...', 
            edit: processingMsg.key 
        });

        // Send the resulting image with validation
        await conn.sendMessage(m.chat, { 
            image: { url: resultUrl }, 
            caption: `✨ *Nano-Banana AI Result*\n\n*Prompt:* ${text}\n*Processed by:* @${m.sender.split('@')[0]}`,
            mentions: [m.sender]
        }, { quoted: m });

        // Delete processing message
        try {
            await conn.sendMessage(m.chat, { delete: processingMsg.key });
        } catch (e) {
            // Ignore delete error
        }

    } catch (error) {
        console.error('Error in Nano-Banana plugin:', error);
        
        // Delete processing message if any
        if (processingMsg) {
            try {
                await conn.sendMessage(m.chat, { delete: processingMsg.key });
            } catch (e) {
                // Ignore delete error
            }
        }

        // Define user-friendly error message
        let errorMessage = '🚨 An error occurred: ';
        
        if (error.name === 'AbortError' || error.message.includes('timeout')) {
            errorMessage += 'Request timeout. Server took too long to respond, please try again later.';
        } else if (error.message.includes('socket hang up') || error.message.includes('ECONNRESET')) {
            errorMessage += 'Connection was lost. Please try again in a moment.';
        } else if (error.message.includes('invalid parameter')) {
            errorMessage += 'Invalid parameter. Make sure the image and prompt are correct.';
        } else if (error.message.includes('HTTP 429')) {
            errorMessage += 'Too many requests. Please wait a moment and try again.';
        } else if (error.message.includes('HTTP 500')) {
            errorMessage += 'Server is having issues. Please try again later.';
        } else if (error.message.includes('HTTP')) {
            errorMessage += `Server error: ${error.message}`;
        } else {
            errorMessage += error.message || 'Unknown error';
        }

        await m.reply(errorMessage + '\n\n💡 *Tips:*\n• Ensure the image is no more than 10MB\n• Use prompts in English\n• Try again in a few minutes if the server is busy');
    }
};

handler.help = ['nanobanana <prompt>'];
handler.tags = ['ai', 'tools'];
handler.command = ['nanobanana', 'nb'];
handler.limit = true;
handler.premium = false;

export default handler;